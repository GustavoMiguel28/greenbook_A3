    private void logar() {                                               
        try {
            String login_usuario, senha_usuario;
        
            login_usuario = txtLoginUsuario.getText();
            senha_usuario = new String(txtSenhaUsuario.getPassword());
        
            UsuarioDTO objusuariodto = new UsuarioDTO();
            objusuariodto.setLogin_usuario(login_usuario);
            objusuariodto.setSenha_usuario(senha_usuario);
            
            UsuarioDAO objusuariodao = new UsuarioDAO();
            ResultSet rsusuariodao = objusuariodao.autenticacaoUsuario(objusuariodto);
            
            if (rsusuariodao.next()) {
                if (login_usuario.equals("admin")) {
                    frmAdmHomeVIEW objfrmadmhomeview = new frmAdmHomeVIEW();
                    objfrmadmhomeview.setVisible(true);
                
                    dispose();
                
                } else {
                    UsuarioDTO objusuariodto_completo = new UsuarioDTO();
                    objusuariodto_completo = objusuariodao.buscarDadosUsuario(objusuariodto);
                    
                    frmUserBibliotecaVIEW objfrmhomeview = new frmUserBibliotecaVIEW(objusuariodto_completo);
                    objfrmhomeview.setVisible(true);
                
                    dispose();
                }
                
            } else {
                JOptionPane.showMessageDialog(null, "Usu√°rio/Senha incorreto(s)");
            }
            
        } catch (SQLException erro) {
            JOptionPane.showMessageDialog(null, "frmLoginVIEW" + erro);
        }
        
    }
